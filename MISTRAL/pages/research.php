<h2>Research</h2>

<p>
We work on network modelling of human diseases at the top level, specifically in cancer. All the projects we contribute consist of steps starting at the data production, computational modeling and hypothesis generations, and finally experimental validation. We have experience both in structural bioinformatics and computational systems biology. These two different but complementing areas are combined and improved to address new questions in the systems biology area in our research group.</p>


