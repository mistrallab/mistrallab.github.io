<p>Obviously, biology is still moving toward generation of big data. No single data type is capable to fully represent the cellular activity. Beyond the list of molecules from each data type, there is a necessity to consider multiple data jointly and reconstruct the relations between these molecules which implies the necessity of personalized network-based approaches. We develop new integrative network modelling approaches to combine multiple high-throughput data with interactomes. Our current and planned research covers several branches of the systems biology of diseases. The main topics can be listed as follows: 

</p>

<ul>
<li>revealing tumour specific structural networks by integrating alternative splicing events, </li>
<li>modelling temporal signalling networks in tumour tissues,  </li>
<li>analysis of temporal ischemic effects at sample freezing stage in ovarian cancer,  </li>
<li>integrating multi omic data to reconstruct patient-specific cancer pathways.  </li>
</ul>



