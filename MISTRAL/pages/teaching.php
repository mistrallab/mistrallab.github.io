<li>improve writing and presentation skills.</li>
</ul>
<p><b><a href="https://catalog.metu.edu.tr/course.php?course_code=9080500" target="_blank">BIN500 - Programming for Informatics </a></b></p>
<p>The course is an introduction to scientific computation and computer programming for graduate students with no prior programming background. Students will learn how to solve scientific problems with programming, how to write a complete program that gets inputs, perform computations and gives outputs by using loops, functions, and different data types. The course covers scientific problems from different areas to be solved with Python programs, which is a popular, general-purpose, open-source language and widely used in several areas of informatics in both industry and research fields.</p>
<p>The course objective is to provide first knowledge and basic skills of programming with Python.</p>
<ul>
<li>Introduction to programming concepts using Python</li>
<li>Understanding the data types and structures.</li>
<li>Writing programs by using Python.</li>
<li>Solving problems with scientific computations in Python.</li>
<li>Debugging programs. </li>
</ul>
