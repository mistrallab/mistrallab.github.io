<p>This course provides an in-depth review of the publicly available software tools and biological databases. Different types of biological data will be introduced and techniques for organization of biological data will be discussed. Also, the course will cover extensive use of web- based bioinformatics environments for investigation and analysis of biological data. <br> At the end of this course, the student will</p>
<ul>	
