<p><b><a href="https://catalog.metu.edu.tr/course.php?course_code=9080515" target="_blank">BIN515 - Structural Bioinformatics </a></b></p>
<p>
The course is an introduction to structural bioinformatics. Additionally, advanced techniques used in structural bioinformatics will be covered. In the scope of this course, students will learn to analyze, classify, simulate, predict and visualize biological molecules, mainly proteins. We cover the fundamentals of protein structures, structure determination techniques, molecular dynamics simulations, molecular docking and techniques to predict the protein interaction in atomic resolution. 
</p><p>The course objective is to provide fundamentals of structural bioinformatics and computational techniques to analyze molecular structures.</p>
<ul>
<li>Introduction to Structural Bioinformatics</li>
<li>Understanding the protein structures and interactions.</li>
<li>Writing programs to analyze and understand the structures of molecules.</li>
<li>Visualization of the structures. </li>
<li>Learning the theory of molecular docking, simulations and prediction techniques.  </li>
</ul>

