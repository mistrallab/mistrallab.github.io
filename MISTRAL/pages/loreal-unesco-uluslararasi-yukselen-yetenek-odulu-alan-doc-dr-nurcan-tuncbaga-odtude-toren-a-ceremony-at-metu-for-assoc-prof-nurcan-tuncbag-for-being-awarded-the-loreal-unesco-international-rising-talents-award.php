<p> 
	<h3>Women in Science Award in 2019 <br> <h5 float=right> 27.03.2019 </h5></h3>

<br>
    After being awarded the L'Oreal-UNESCO for Women in Science Award in 2018, Assoc. Prof. Nurcan Tuncbag from METU Department of Health Informatics, Graduate School of Informatics, has been recently awarded the L'Oreal-UNESCO International Rising Talent Award in Paris as well. 
In her study on computational models of complex biological systems and looking for an answer to the question "Why some cancer treatments do not work for some people?", and aiming to contribute to the development of the most appropriate personalized treatment strategy for cancer, 
Dr. Tuncbag was one of the 15 people deemed worthy of the award among 275 candidates out of nine thousand applications worldwide.


A ceremony was held at METU for Assoc. Prof. Tuncbag after being awarded the L'Oreal-UNESCO International Rising Talents Award. At the ceremony, Prof. Dr. Mustafa Versan Kok, President of METU, presented a plaque and certificate to Assoc. Prof. Tuncbag for her international achievement.


Delivering a brief speech at the ceremony, President Prof. Mustafa Versan Kok drew attention to young researchers from METU, who gain national and international achievements, and extended his congratulations to Nurcan Tuncbag and her team for bringing such an important international award to our University and to the country.


Then taking the floor to make a brief speech, Assoc. Prof. Tuncbag talked about her studies in the field of structural modeling of protein interactions, revealing how the genome and the networks of interactions among proteins and genes are altered in cells during cancer that enabled her to receive the L'Oreal-UNESCO International Rising Talent Award.
