<h2>Welcome to the Network Modeling Lab!</h2>
<p>High-throughput, omic methods reveal many aspects of cellular state and biological responses to perturbations and diseases. However, taken in isolation, no single type of data is capable to fully represent the cellular activity. Our research direction is toward integrating multiple omic data in a network context and revealing how the networks of interactions among proteins are altered in cells during disease or any perturbations. Instead of reductionism that is targeting just one molecule, I am interested in a whole set of molecules together from a systems biology perspective. Toward this aim, our team is developing new integrative network modelling approaches. These network-based approaches reconstruct the relations between these molecules and elucidate the hidden components of signaling in a condition-specific way. </p>
