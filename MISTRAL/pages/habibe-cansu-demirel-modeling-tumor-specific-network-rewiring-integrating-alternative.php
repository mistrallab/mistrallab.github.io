<h3>Modeling the Tumor Specific Network Rewiring by Integrating Alternative Splicing Events with Structural Interactome <br> <h5 float=right> 31.07.2019 </h5></h3>
<br>
    <b> Abstract:</b> Alternative splicing is a post-transcriptional regulation which is important for the diversity of the proteome and eventually the interactome. 
It enables the production of multiple proteins from a single gene with different structures. 
In a network point of view, these structural changes can introduce new interactions or cause the loss of the existing ones. 
The variations in this mechanism has been associated with various diseases including cancer. 
In this study, we reconstructed patient specific networks with tumor specific protein isoforms by integrating the protein structures and the interaction losses they bring with. 
For this purpose, we collected 400 breast cancer tumors and 112 normal RNA-seq data from the Cancer Genome Atlas (TCGA) and found the transcripts that show increased expression patterns in tumor cells. 
We mapped these transcripts to their available protein isoforms found in UniProt. 
Additionally, we compiled a structural human interactome from multiple sources and aligned the missing residues on isoforms with the known/predicted protein interfaces to find potential interaction losses. 
At the end, we constructed two interactomes for each sample; one filtered based on the lost interfaces as a result of predominant isoforms (called "terminal set") and one filtered based on the expression. 
Then, we used the same terminal set with Omics Integrator to model two sets of networks based on the two patient-specific interactomes. Finally, we compared the resulting two networks and all tumor specific networks simultaneously to reveal pathway, protein-protein interaction and protein patterns that can cluster the tumors according to their similarities. The results of our analysis will contribute to the elucidation of tumor mechanisms and will help for target selection and developing therapeutic strategies.
</p>
